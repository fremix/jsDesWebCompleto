/**
 * Declaracion de funcion ( Function Declaration )
 *
 */

function sumar() {
  console.log(2 + 4);
}

sumar();

function restar() {
  console.log(10 - 5);
}

restar();

function dividir() {
  console.log(9 / 3);
}

dividir();

function multiplicar(a, b) {
  console.log(9 * 9);
}

multiplicar();

/**
 * Expresion de Funciones (Funcion Expresion)
 *
 */

const sumar2 = function () {
  console.log(5 + 1);
};

sumar2();

const multiplicar2 = function () {
  console.log(2 * 3);
};

multiplicar2();

// Es similiar a variable
const dividir2 = function () {
  console.log(50 / 50);
};

dividir2();

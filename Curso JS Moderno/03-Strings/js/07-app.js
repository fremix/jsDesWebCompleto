// String Methods - Convertir a Mayusculas o Minusculas

// Para convertir todo a MAYUSCULAS .toUppercase()

const producto = 'Monitor de 55 Pulgadas';
console.log(producto.toUpperCase());

// Transformar todo a minusculas .toLowerCase()

console.log(producto.toLocaleLowerCase());

// Otro ejemplo de convertir todo a minusculas

const email = 'MICORREO@CORREO.COM';
console.log(email.toLowerCase());


// Convertir un numero a String
const precio = 300;
console.log(precio);
console.log(precio.toString()); // con .toString() el numero se convierte a String
